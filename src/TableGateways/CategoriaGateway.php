<?php
namespace Src\TableGateways;

class CategoriaGateway {

    private $db = null;

    public function __construct($db)
    {
        $this->db = $db;
    }

    public function findAll()
    {
        $statement = "
            SELECT 
                id, categoria_nombre, categoria_atributo
            FROM
                categoria;
        ";

        try {
            $statement = $this->db->query($statement);
            $result = $statement->fetchAll(\PDO::FETCH_ASSOC);
            return $result;
        } catch (\PDOException $e) {
            exit($e->getMessage());
        }
    }

    public function find($id)
    {
        $statement = "
            SELECT 
                id, categoria_nombre, categoria_atributo
            FROM
                categoria
            WHERE id = ?;
        ";

        try {
            $statement = $this->db->prepare($statement);
            $statement->execute(array($id));
            $result = $statement->fetchAll(\PDO::FETCH_ASSOC);
            return $result;
        } catch (\PDOException $e) {
            exit($e->getMessage());
        }    
    }

    public function insert(Array $input)
    {
        $statement = "
            INSERT INTO categoria 
                (categoria_nombre, categoria_atributo)
            VALUES
                (:categoria_nombre, :categoria_atributo);
        ";

        try {
            $statement = $this->db->prepare($statement);
            $statement->execute(array(
                'categoria_nombre' => $input['categoria_nombre'],
                'categoria_nombre'  => $input['categoria_nombre'],
            ));
            return $statement->rowCount();
        } catch (\PDOException $e) {
            exit($e->getMessage());
        }    
    }

    public function update($id, Array $input)
    {
        $statement = "
            UPDATE categoria
            SET 
                categoria_nombre = :categoria_nombre,
                categoria_atributo  = :categoria_atributo
            WHERE id = :id;
        ";

        try {
            $statement = $this->db->prepare($statement);
            $statement->execute(array(
                'id' => (int) $id,
                'categoria_nombre' => $input['categoria_nombre'],
                'categoria_atributo'  => $input['categoria_atributo'],
            ));
            return $statement->rowCount();
        } catch (\PDOException $e) {
            exit($e->getMessage());
        }    
    }

    public function delete($id)
    {
        $statement = "
            DELETE FROM categoria
            WHERE id = :id;
        ";

        try {
            $statement = $this->db->prepare($statement);
            $statement->execute(array('id' => $id));
            return $statement->rowCount();
        } catch (\PDOException $e) {
            exit($e->getMessage());
        }    
    }
}